package oopHomework;

public class Course {
	int id;
	String name;
	String teacher;
	int completionRate;
	
	public Course() {
		
	}
	
	public Course(int id, String name, String teacher, int completionRate) {
		this.id = id;
		this.name = name;
		this.teacher = teacher;
		this.completionRate = completionRate;
		
	}

}
